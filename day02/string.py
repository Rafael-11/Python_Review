# slicing string is same substring in Java

s = 'Java Programming language'
s2=s[5:16]
print(s2)

s3=s[:4]
print(s3)

s4=s[:-1]
print(s4)

s5=s[17:-1]
print(s5)

# reverse the string after slicing
s6=s[::-1]
print(s6)

# reverse string in python with function
s7 = 'Python Programming'
reversed(s7)
print(s7)

